package cn.bjeastearth.waterapp.model;

public class CountRiverYear {
	private String Name;
	private int ChCount;
	private int HhCount;
	private int LjhCount;
	private int SanCount;
	private int SiCount;
	private int WuCount;
	private int LiuCount;
	private String YearName ;
	
	public int getSanCount() {
		return SanCount;
	}
	public void setSanCount(int sanCount) {
		SanCount = sanCount;
	}
	public int getSiCount() {
		return SiCount;
	}
	public void setSiCount(int siCount) {
		SiCount = siCount;
	}
	public int getWuCount() {
		return WuCount;
	}
	public void setWuCount(int wuCount) {
		WuCount = wuCount;
	}
	public int getLiuCount() {
		return LiuCount;
	}
	public void setLiuCount(int liuCount) {
		LiuCount = liuCount;
	}
	public int getChCount() {
		return ChCount;
	}
	public void setChCount(int chCount) {
		ChCount = chCount;
	}
	public int getHhCount() {
		return HhCount;
	}
	public void setHhCount(int hhCount) {
		HhCount = hhCount;
	}
	public int getLjhCount() {
		return LjhCount;
	}
	public void setLjhCount(int ljhCount) {
		LjhCount = ljhCount;
	}
	public String getYearName() {
		return YearName;
	}
	public void setYearName(String yearName) {
		YearName = yearName;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
}
